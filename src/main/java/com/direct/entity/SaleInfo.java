package com.direct.entity;

import java.util.Date;

public class SaleInfo {
	private int sid;
	private EmployeeInfoEntity empInfo;
	private CustomerInfo cusInfo;
	private CarInfo carInfo;
	private Date saleTime;
	private double advance;
	private double receipts;
	private double receivable;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public EmployeeInfoEntity getEmpInfo() {
		return empInfo;
	}
	public void setEmpInfo(EmployeeInfoEntity empInfo) {
		this.empInfo = empInfo;
	}
	public CustomerInfo getCusInfo() {
		return cusInfo;
	}
	public void setCusInfo(CustomerInfo cusInfo) {
		this.cusInfo = cusInfo;
	}
	public CarInfo getCarInfo() {
		return carInfo;
	}
	public void setCarInfo(CarInfo carInfo) {
		this.carInfo = carInfo;
	}
	public Date getSaleTime() {
		return saleTime;
	}
	public void setSaleTime(Date saleTime) {
		this.saleTime = saleTime;
	}
	public double getAdvance() {
		return advance;
	}
	public void setAdvance(double advance) {
		this.advance = advance;
	}
	public double getReceipts() {
		return receipts;
	}
	public void setReceipts(double receipts) {
		this.receipts = receipts;
	}
	public double getReceivable() {
		return receivable;
	}
	public void setReceivable(double receivable) {
		this.receivable = receivable;
	}
	
	
	
}
