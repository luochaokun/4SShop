package com.direct.entity;

import java.util.Date;

public class ExpenditureEntity {
	private int eid;
	private OutTypeEntity outType;
	private EmployeeInfoEntity staff;
	private String money;
	private Date expTime;
	private String account;
	private String remark;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public OutTypeEntity getOutType() {
		return outType;
	}
	public void setOutType(OutTypeEntity outType) {
		this.outType = outType;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public Date getExpTime() {
		return expTime;
	}
	public void setExpTime(Date expTime) {
		this.expTime = expTime;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public EmployeeInfoEntity getStaff() {
		return staff;
	}
	public void setStaff(EmployeeInfoEntity staff) {
		this.staff = staff;
	}
	
	
}
