package com.direct.entity;

import java.util.Date;

public class KaoQinInfoEntity {
	private int id; //编号
	private EmployeeInfoEntity emp; //员工表
	private Date time; //考勤日期
	private Date startJobTime; //上班时间
	private Date startCardTime; //上班打卡时间
	private Date stopJobTime; //下班时间
	private Date stopCardTime; //下班打卡时间
	private String jobState; //出勤状态
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public EmployeeInfoEntity getEmp() {
		return emp;
	}
	public void setEmp(EmployeeInfoEntity emp) {
		this.emp = emp;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public Date getStartJobTime() {
		return startJobTime;
	}
	public void setStartJobTime(Date startJobTime) {
		this.startJobTime = startJobTime;
	}
	public Date getStartCardTime() {
		return startCardTime;
	}
	public void setStartCardTime(Date startCardTime) {
		this.startCardTime = startCardTime;
	}
	public Date getStopJobTime() {
		return stopJobTime;
	}
	public void setStopJobTime(Date stopJobTime) {
		this.stopJobTime = stopJobTime;
	}
	public Date getStopCardTime() {
		return stopCardTime;
	}
	public void setStopCardTime(Date stopCardTime) {
		this.stopCardTime = stopCardTime;
	}
	public String getJobState() {
		return jobState;
	}
	public void setJobState(String jobState) {
		this.jobState = jobState;
	}
}
